package java_exercise.service;

public class Calculator {

    public int add(int a, int b) {
        int i = a + b;
        return i;
    }

    public int sub(int a, int b) {
        int i = a - b;
        return i;
    }

    public int multi(int a, int b) {
        int i = a * b;
        return i;
    }

    public double divide(int a, int b) {
        double i = (double) a / b;
        return i;
    }

}
